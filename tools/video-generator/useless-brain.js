const _profileCache = new Map();

function getAbsurdProfile(app, opts) {
  const name = safe(app.name, 90) || 'Useless App';
  const fresh = opts && opts.fresh;
  const cacheKey = name + '::' + safe(app.file || app.url || '', 200);

  if (!fresh && _profileCache.has(cacheKey)) {
    return _profileCache.get(cacheKey);
  }

  const scoreReport = calculateUselessnessScore(app);
  const sentience = measureAppSentience(app);

  const profile = {
    name,
    uselessness: scoreReport.score,
    grade: scoreReport.grade,
    confidenceInterval: scoreReport.confidenceInterval,
    modelVersion: scoreReport.modelVersion,
    sentiencePercent: sentience.sentiencePercent,
    sentienceStatus: sentience.status,
    sentienceThought: sentience.lastKnownThought,
    category: (app && app.fakeCategory) || pick(CATEGORIES),
    dangerLevel: pick(DANGER_LEVELS),
    award: pick(AWARDS),
    stat: pick(STATS)(),
    review: pick(REVIEWS),
    warning: pick(WARNINGS),
    signals: scoreReport.signals
  };

  _profileCache.set(cacheKey, profile);
  return profile;
}

