function pick(items) {
  return items[Math.floor(Math.random() * items.length)];
}

function pickN(items, n) {
  const copy = items.slice();
  for (let i = copy.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy.slice(0, Math.max(0, Math.min(n, copy.length)));
}

function safe(input, max = 5000) {
  return String(input || '')
    .replace(/\s+/g, ' ')
    .replace(/[<>]/g, '')
    .trim()
    .slice(0, max);
}

function escapeHtml(input) {
  // The chaos panel injects app data into the page. Names come from apps.json,
  // so this is mild paranoia, but free paranoia.
  return String(input || '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function fakeNumber(min, max) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

