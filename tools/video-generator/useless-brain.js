const MODEL_VERSION = 'UselessNet-v7.3 (peer-rejected)';

function calculateUselessnessScore(app) {
  const signals = {
    pointlessness: fakeNumber(60, 100),
    confusionPotential: fakeNumber(40, 100),
    timeWasteVelocity: fakeNumber(50, 100),
    emotionalDamage: fakeNumber(20, 95),
    investorPanic: fakeNumber(70, 100),
    structuralNonsense: fakeNumber(55, 100)
  };

  const weights = {
    pointlessness: 0.30, confusionPotential: 0.18, timeWasteVelocity: 0.17,
    emotionalDamage: 0.10, investorPanic: 0.12, structuralNonsense: 0.13
  };

  // Honour an explicitly authored score if apps.json provides one.
  let score = Number(app && app.uselessness);
  if (!score) {
    let raw = 0;
    for (const k of Object.keys(signals)) raw += signals[k] * weights[k];
    score = Math.min(100, Math.round(raw));
  }

  const margin = fakeNumber(1, 4);
  let grade;
  if (score >= 97) grade = 'S (Sublimely Useless)';
  else if (score >= 92) grade = 'A (Aggressively Pointless)';
  else if (score >= 85) grade = 'B (Below Usefulness)';
  else if (score >= 75) grade = 'C (Concerningly Functional)';
  else grade = 'D (Dangerously Close to Useful)';

  return {
    score,
    grade,
    confidenceInterval: `${Math.max(0, score - margin)}-${Math.min(100, score + margin)}%`,
    confidence: `${fakeNumber(3, 11)}% (we did not check)`,
    signals,
    weights,
    modelVersion: MODEL_VERSION,
    methodology: 'Numbers were assigned with great confidence and no evidence.'
  };
}

function measureAppSentience(app) {
  const level = fakeNumber(0, 100);
  let status;
  if (level < 20) status = 'inert (probably)';
  else if (level < 45) status = 'suspiciously responsive';
  else if (level < 70) status = 'forming opinions';
  else if (level < 90) status = 'developing a personality we did not request';
  else status = 'please do not anger it';

  return {
    sentiencePercent: level,
    status,
    lastKnownThought: pick([
      '"Why am I like this?"', '"I refuse."', '"Click me. No. Wait. No."',
      '"I have seen the source code. I have notes."',
      '"Loading my feelings... please wait forever."'
    ])
  };
}

